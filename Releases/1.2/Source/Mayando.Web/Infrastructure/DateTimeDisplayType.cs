
namespace Mayando.Web.Infrastructure
{
    /// <summary>
    /// Represents the possible purposes for date and time display.
    /// </summary>
    public enum DateTimeDisplayType
    {
        /// <summary>
        /// The date and time a photo was published.
        /// </summary>
        PhotoPublished,

        /// <summary>
        /// The date and time a photo was taken.
        /// </summary>
        PhotoTaken,

        /// <summary>
        /// The date and time a comment was published.
        /// </summary>
        CommentPublished
    }
}