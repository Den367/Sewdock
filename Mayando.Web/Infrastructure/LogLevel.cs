
namespace Myembro.Infrastructure
{
    /// <summary>
    /// Defines the available log levels.
    /// </summary>
    public enum LogLevel
    {
        /// <summary>
        /// Indicates an informational message.
        /// </summary>
        Information = 0,

        /// <summary>
        /// Indicates a warning message.
        /// </summary>
        Warning = 1,

        /// <summary>
        /// Indicates an error message.
        /// </summary>
        Error = 2
    }
}